        private void button1_Click(object sender, EventArgs e)
        {
            neural = new PerceptronNeuralNetwork();
            neural.setTrainingLayerPatter(getPreData());
            neural.setTrainingCharacterRecognition(getPreCharacterData());
            neural.initializeOutputLayer();
            neural.initializeWeights();

            if (neural.trainPerceptron() == true)
            {
                MessageBox.Show("Training Successful Vowel / Consonant");
            }

            if (neural.trainPerceptronCharacterRecognition() == true)
            {
                MessageBox.Show("Training Successful Character Recognition");
            }
        }

